##获取字典视图

scores={'陈文钦':100,'宸宸宸':80,'陈伟':10}
keys=scores.keys()
print(keys)
print(type(keys))
print(list(keys))

print('————————————————————————————')
values=scores.values()
print(values)
print(type(values))
print(list(values))

print('————————————————————————————')
items=scores.items()
print(items)
print(type(items))
print(list(items))
