import turtle as t
screen=t.Screen()
screen.screensize(bg="white")
screen.setup(800,800)
t.speed(3)
t.pensize(3)

t.goto(0,-200)
t.begin_fill()
t.color("red")
t.circle(radius=150)
t.end_fill()

t.color("brown")
t.up()
t.goto(-90,0)
t.pd()
t.circle(180,40)
t.seth(105)
t.up()
t.goto(-20,20)
t.pd()
t.circle(180,50)
t.pd()

t.begin_fill()
t.color("green")
t.circle(180,50)
t.seth(-30)
t.circle(180,55)
t.end_fill()

t.begin_fill()
t.seth(0)
t.circle(180,50)
t.seth(-180)
t.circle(180,50)

t.end_fill()

t.done()