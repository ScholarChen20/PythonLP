s='hello,worlD'
a='Wo,Shi,NI,DIE'
b='you are a good man'
print('——————————————所有字符转大写———————————————')
print(s.upper())
print(a.upper())
print('——————————所有字符转小写———————————————')
print(a.lower())
print('————————大写转小写，小写转大写—————————————')
print(a.swapcase())
print(s.swapcase())
print('————————————第一个字符大写，其余装小写———————————————')
print(s.capitalize())
print('—————————————每个单词首字符转大写，其余部分小写———————————')
print(b.title())
